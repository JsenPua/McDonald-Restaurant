    private void jbtEditCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtEditCartActionPerformed
        int[] listInt = jlstCart.getSelectedIndices();
        if (listInt.length > 1) {
            JOptionPane.showMessageDialog(null, "System can only edit 1 item at a time!");
        }
        OrderList ord = cart.getOrderItem(listInt[0]);
        if (ord != null) {
            itemEdit.removeAll();
            itemEdit.revalidate();
            itemEdit.repaint();
            itemEdit = new ItemDescFrame(ord.getItem(), cart);
        } else {
            JOptionPane.showMessageDialog(null, "Nothing is selected!");

        }

    }//GEN-LAST:event_jbtEditCartActionPerformed

    private void jbtCheckOutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtCheckOutActionPerformed

        if (cart.getFinalCart().size() != 0) {
            payment = new PaymentFormPanel(cart.getFinalCart(), stf);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Please insert Item to cart.", "Cart is Empty", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jbtCheckOutActionPerformed

