    public MaintaiItemFrame(String staff) {
        stf = staff;
        transControl = new TransactionControl();
        transID = transControl.getAfterLastCode();
        cart = new Cart(transID);
        jlstCart = new JList(cart.getCartData());

        initComponents();
        setLocationRelativeTo(null);
        int delay = 500;
        tempCount = OrderList.getNumOfOrder();
        ActionListener taskPerformer = new ActionListener() {
            public void actionPerformed(ActionEvent evt) {
                orderCount = OrderList.getNumOfOrder();
                if (cart.getCounter() >= 0 && orderCount != tempCount) {
                    jlstCart = new JList(cart.getCartData());
                    jpEast.setViewportView(jlstCart);
                    tempCount++;
                }
            }
        };
        new Timer(delay, taskPerformer).start();
        pack();
        setResizable(false);
        setLocationRelativeTo(null);
        setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        setVisible(true);
        setTitle("Mcd Ordering System");
    }

