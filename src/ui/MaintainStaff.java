    public void Position(String id) {
        domain.Staff staff = stfControl.selectRecord(id);

        if (staff.getPositon().equals("Manager")) {
            jcbStaff.setSelected(false);
            jcbManager.setSelected(true);

        } else {
            jcbStaff.setSelected(true);
            jcbManager.setSelected(false);

        }

    }

    public String GeneratePosition(String id) {
        String position = "";
        if (id.charAt(0) == 'M') {
            position = "Manager";
        } else {
            position = "Staff";
        }
        return position;
    }

    public String GenarateId() {
        String id = "";
        int a = (int) (Math.random() * 9999);
        if (a < 10 && jcbManager.isSelected()) {

            id = "M00" + a;
        } else if (a < 10000 && jcbManager.isSelected()) {

            id = "M" + a;
        } else if (a < 100 && jcbManager.isSelected()) {

            id = "M0" + a;
        } else if (a < 1000 && jcbManager.isSelected()) {

            id = "M" + a;
        } else if (a < 10 && jcbStaff.isSelected()) {

            id = "S00" + a;
        } else if (a < 100 && jcbStaff.isSelected()) {

            id = "S0" + a;
        } else if (a < 1000 && jcbStaff.isSelected()) {

            id = "S" + a;
        } else if (a < 10000 && jcbStaff.isSelected()) {

            id = "S" + a;
        }
        return id;
    }

