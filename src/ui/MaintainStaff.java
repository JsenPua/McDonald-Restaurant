    private void jbtUpdate1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtUpdate1ActionPerformed

        String id = jtfID.getText();

        domain.Staff staff = stfControl.selectRecord(id);
        
         if (jcbManager.isSelected() == false && jcbStaff.isSelected() == false) {
            jlClick.setVisible(true);
        }
        if (jlIcon.getIcon() == null) {
            jlAdd.setVisible(true);
        }
        
         if (jtfName.getText().equals("") || jtfIc.getText().equals("") || jtfContact.getText().equals("") || jtaAddress.getText().equals("")
                || jtfSalary.getText().equals("")
                || jpfPassword.getText().equals("") || jpfPassword.getText().length() < 4 || jlIcon.getIcon() == null) {
            JOptionPane.showMessageDialog(null, "Please fill in all the information", "Incomplete", JOptionPane.INFORMATION_MESSAGE);
        }

       
        
        
         else{
             if(staff != null) {
            String position = "";
            domain.Staff stf = (new StaffDA().getRecord(jtfSearch.getText()));
            double salary = Double.parseDouble(jtfSalary.getText());
            Icon icon = new Icon();
            if (jcbManager.isSelected()) {
                position = "Manager";
            } else if (jcbStaff.isSelected()) {
                position = "Staff";
            }

            try {
                String selectedFile = jfcPhoto.getSelectedFile().getAbsolutePath();

                ImageIcon imageIcon = new ImageIcon(selectedFile);

                URL url = new URL("file:///" + selectedFile);

                icon = new Icon(imageIcon, url);

            } catch (NullPointerException ex) {
                ImageIcon imageIcon = staff.getIcon().getImage();
                icon = new Icon(imageIcon);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "exception occurs" + ex.getMessage());
            }

            int option = JOptionPane.showConfirmDialog(null, "Confirm update " + jtfName.getText() + "'s information?", "Update Employee", JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION) {
                stf = new domain.Staff(id, jtfIc.getText(), jtfName.getText(), jpfPassword.getText(), jtfContact.getText(), jtaAddress.getText(), position, salary, icon);
                try {
                    stfControl.updateRecord(stf);
                } catch (SQLException | IOException ex) {
                    Logger.getLogger(MaintainStaff.class.getName()).log(Level.SEVERE, null, ex);
                }
                JOptionPane.showMessageDialog(null, jtfName.getText() + "'s information updated successfully");
                Clear();
            } else {

            }
             }
         else {
            JOptionPane.showMessageDialog(null, "Employee record not found .", "RECORD NOT FOUND", JOptionPane.ERROR_MESSAGE);
        }
         }
    }//GEN-LAST:event_jbtUpdate1ActionPerformed

    private void jbtCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtCreateActionPerformed
        String id = "";
        String position = "";
        boolean decision = false;
        domain.Staff staff = stfControl.selectRecord(id);
        if(!jtfID.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please Enter the clear button", "Clean", JOptionPane.INFORMATION_MESSAGE);
        
        if (jtfName.getText().equals("") || jtfIc.getText().equals("") || jtfContact.getText().equals("") || jtaAddress.getText().equals("")
                || jtfSalary.getText().equals("")
                || jpfPassword.getText().equals("") || jpfPassword.getText().length() < 4 || jlIcon.getIcon() == null) {
            JOptionPane.showMessageDialog(null, "Please fill in all the information", "Incomplete", JOptionPane.INFORMATION_MESSAGE);
        }

        if (jcbManager.isSelected() == false && jcbStaff.isSelected() == false) {
            jlClick.setVisible(true);
        }
        if (jlIcon.getIcon() == null) {
            jlAdd.setVisible(true);

        }
        }else {
            do {
                id = GenarateId();
                position = GeneratePosition(id);
                staff = stfControl.selectRecord(id);
                if (staff == null) {
                    double salary = Double.parseDouble(jtfSalary.getText());
                    decision = true;

                    try {

                        String selectedFile = jfcPhoto.getSelectedFile().getAbsolutePath();
                        ImageIcon imageIcon = new ImageIcon(selectedFile);

                        URL url1 = new URL("file:///" + selectedFile);
                        Icon icon = new Icon(imageIcon, url1);

                        staff = new domain.Staff(id, jtfName.getText(), jpfPassword.getText(), jtfContact.getText(), jtaAddress.getText(), jtfIc.getText(), position, salary, icon);

                    } catch (Exception ex) {
                        JOptionPane.showMessageDialog(null, "exception occurs" + ex.getMessage());
                    }

                }
                int option = JOptionPane.showConfirmDialog(null, "Confirm adding as employee ?", "New Employee", JOptionPane.YES_NO_OPTION);
                if (option == JOptionPane.YES_OPTION) {
                    stfControl.addRecord(staff);
                    JOptionPane.showMessageDialog(null, "Your ID is " + id);

                    Clear();
                }

            } while (decision != true);
        }
    }//GEN-LAST:event_jbtCreateActionPerformed

    private void jbtDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtDeleteActionPerformed
        String id = jtfID.getText();
        domain.Staff stf = stfControl.selectRecord(id);

        if (stf != null) {
            jbtRemove.setEnabled(false);
            jtfIc.setEnabled(false);
            jbtAdd.setEnabled(false);
            jtfContact.setEnabled(false);
            jpfPassword.setEnabled(false);
            jtaAddress.setOpaque(false);
            jtaAddress.setEnabled(false);
            jcbStaff.setEnabled(false);
            jcbManager.setEnabled(false);
            jtfSalary.setEnabled(false);
            jtfName.setEnabled(false);
            jtfIc.setText(stf.getIc());
            jtfName.setText(stf.getName());
            jpfPassword.setText(stf.getPassword());
            jtfContact.setText(stf.getContactNo());
            jtaAddress.setText(stf.getAddress());
            Position(id);
            jlIcon.setIcon(stf.getIcon().getImage());

            int option = JOptionPane.showConfirmDialog(null, "Do you wish to remove " + jtfName.getText() + "?", "Delete Employee", JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION) {
                stfControl.deleteRecord(stf);
                JOptionPane.showMessageDialog(null, jtfName.getText() + " successfully delete");

                Clear();

            } else {
                jtfIc.setEnabled(true);
                jbtAdd.setEnabled(true);
                jbtRemove.setEnabled(true);
                jtfContact.setEnabled(true);
                jpfPassword.setEnabled(true);
                jtaAddress.setOpaque(true);
                jtaAddress.setEnabled(true);
                jcbStaff.setEnabled(true);
                jcbManager.setEnabled(true);
                jtfSalary.setEnabled(true);
                jtfName.setEnabled(true);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Invalid user ID ", "STAFF NOT FOUND", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jbtDeleteActionPerformed

